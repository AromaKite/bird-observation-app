export function lookupSpecies(jaName: string): string {
  const entry = BIRD_DATABASE.find(
    (b) => b.ja === jaName || b.ja.includes(jaName) || jaName.includes(b.ja)
  )
  return entry?.species ?? ''
}

export function searchBirds(query: string): BirdEntry[] {
  if (!query) return []
  const q = query.toLowerCase()
  return BIRD_DATABASE.filter((b) => b.ja.includes(query) || b.species.toLowerCase().includes(q)).slice(0, 8)
}
